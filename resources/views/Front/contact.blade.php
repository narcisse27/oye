<script>
    function linkOver()
    {
        document.getElementById('yellow-menu-elipse-2').classList.remove('loop');
        document.getElementById('yellow-menu-elipse-2').classList.add('focus');
        document.getElementById('yellow-menu-elipse-1').classList.remove('loop');
        document.getElementById('yellow-menu-elipse-1').classList.add('focus');
    }
    function linkLeave()
    {
        document.getElementById('yellow-menu-elipse-2').classList.remove('focus');
        document.getElementById('yellow-menu-elipse-2').classList.add('loop');
        document.getElementById('yellow-menu-elipse-1').classList.remove('focus');
        document.getElementById('yellow-menu-elipse-1').classList.add('loop');
    }
    function activePreloader()
    {
        document.getElementById('yellow-menu-wrap').classList.remove('loop');
        document.getElementById('yellow-menu-wrap').classList.remove('focus');
        document.getElementById('yellow-menu-wrap').classList.add('preloader-active');
    }
    function goToHome(){
        activePreloader();
        setInterval(function(){
            window.location = "/";
        }, 1000);
    }
    function goToHomeWithWaitingAndMessage(){
        activePreloader();
        document.getElementById('success-message').classList.remove('hidden');
        document.getElementById('success-message').classList.add('active');
        setInterval(function(){
            var home = "/";
            window.location = home;
        }, 2000);
    }
    $('a').click(function (e) {
        e.preventDefault();
        activePreloader();
        setInterval(function(){
            document.getElementById('success-message').classList.remove('active');
            document.getElementById('success-message').classList.add('hidden');
        }, 800);
        setInterval(function(){
            var home = "/";
            window.location = home;
        }, 1600);
    });
    var form = document.getElementById('contact-form-html');
    if (form.attachEvent) {
        form.attachEvent("submit", sendMessage);
    } else {
        form.addEventListener("submit", sendMessage);
    }
    function validateEmail(email) {
        var re = /^(([^<>()\[\]\\.,;:\s@"]+(\.[^<>()\[\]\\.,;:\s@"]+)*)|(".+"))@((\[[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\])|(([a-zA-Z\-0-9]+\.)+[a-zA-Z]{2,}))$/;
        return re.test(String(email).toLowerCase());
    }
    function sendMessage(e){
        if (e.preventDefault) e.preventDefault();
        var error = false;
        if(document.getElementById('_name').value.length < 3){
            error = true;
            if ( !(document.getElementById('name_label').innerHTML.indexOf("*") >= 0)) {
                document.getElementById('name_label').innerHTML = "Nom, Prénom  * semble incorrecte";
            }else{
                document.getElementById('name_label').innerHTML = "Nom, Prénom";
            }
        }
        if (!validateEmail(document.getElementById('_email').value)) {
            error = true;
            if ( !(document.getElementById('email_label').innerHTML.indexOf("*") >= 0)){
                document.getElementById('email_label').innerHTML = "E-Mail * est incorrect";
            }else{
                document.getElementById('email_label').innerHTML = "E-mail";
            }
        }
        if(document.getElementById('_message').value.length < 10 ){
            error = true;
            if (!( document.getElementById('message_label').innerHTML.indexOf("*") >= 0)) {
                document.getElementById('message_label').innerHTML = "Message * minimum 10 caractères";
            }else{
                document.getElementById('message_label').innerHTML = "Message";
            }
        }
        $.ajaxSetup({
            headers: {
                'X-CSRF-TOKEN': $('meta[name="csrf-token"]').attr('content')
            }
        });
        if(error === false) {
            $.ajax({
                url: "/api/sendMailFromFront",
                type: "POST", //First change type to method here
                dataType: 'json',
                data: {
                    name: document.getElementById('_name').value,
                    email: document.getElementById('_email').value,
                    message: document.getElementById('_message').value,
                    _token: $('meta[name="csrf-token"]').attr('content')
                },
                success: function (response) {
                    goToHomeWithWaitingAndMessage();
                },
                error: function () {
                }
            });
        }
    }


</script>
